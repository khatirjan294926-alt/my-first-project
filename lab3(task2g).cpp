#include <iostream>
using namespace std;

int main()
{
    // a. Declare variables
    float x, y;

    // b. Prompt user to enter value of x
    cout << "Enter value of x: ";
    cin >> x;

    // c. Calculate y (example equation y = 2x + 5)
    y = 2 * x + 5;

    // d. Display result
    cout << "Value of y = " << y << endl;

    return 0;
}